#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	j;
	size_t	i;

	j = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[j] = s2[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
