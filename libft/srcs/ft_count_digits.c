#include "libft.h"

size_t	ft_count_digits(long long n)
{
	size_t	len;

	if (n <= 0)
		len = 1;
	else
		len = 0;
	while (n >= 1 || n <= -1)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
